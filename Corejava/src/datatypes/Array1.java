package datatypes;

public class Array1 {

	public static void main(String[] args) {
		int a[]=new int[3];
		a[0]=1;
		a[2]=10;
		a[1]=12;
		
		System.out.println(a[2]);  //10
		
		System.out.println(a.length); //3
		
		int x[]={1,2,3,4,5};  //5 integer array
		
		System.out.println(x[3]);
  
		System.out.println(x.length);
	}

}
